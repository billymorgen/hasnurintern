    <span style="font-weight: normal;" class = "trespon">
    Di SMA, mungkin kamu akrab dengan istilah Sumbangan Pembinaan Pendidikan (SPP) yang harus dibayar tiap bulan. Di perguruan tinggi juga ada uang yang harus dibayar tiap semester. Di setiap kampus namanya bisa berbeda-beda, ada yang menyebutnya SPP, ada juga yang menamainya BPP. Tapi di Perguruan Tinggi Negeri (PTN) namanya adalah UKT atau Uang Kuliah Tunggal. Apa itu UKT? Yuk, simak penjelasan berikut ini! <br><br>
        <b style="font-size: 40px">Apa itu UKT?</b><br>
Uang Kuliah Tunggal atau UKT <b>adalah biaya kuliah yang wajib dibayarkan oleh mahasiswa setiap semester.</b> Seluruh Perguruan Tinggi Negeri (PTN) di Indonesia sudah menerapkan sistem UKT ini sejak tahun 2013. Hal ini tertuang dalam Permendikbud No 55 Tahun 2013 yang kemudian direvisi dalam Permenristekdikti Nomor 25 Tahun 2020 tentang Standar Biaya Operasional Pendidikan Tinggi pada Perguruan Tinggi Negeri di Lingkungan Kemendikbud.<br>
Dalam program UKT, pemerintah menerapkan sistem subsidi silang sesuai kondisi ekonomi setiap mahasiswa. Semakin baik latar belakang ekonominya, maka akan semakin besar UKT yang dibayarkan. Jadi, mahasiswa dengan kondisi ekonomi yang kuat akan membantu menutupi tagihan biaya kuliah mahasiswa dengan ekonomi yang lebih lemah.<br>
Penerapan sistem subsidi silang dalam UKT ini membuat UKT terbagi menjadi beberapa kelompok. Sistem ini memberi kesempatan bagi mahasiswa dengan berbagai kondisi ekonomi untuk dapat mengenyam pendidikan, bahkan dengan kondisi ekonomi yang kurang.<br>
Apa bedanya sistem UKT dengan pembayaran biasa? Dalam program UKT, mahasiswa hanya perlu membayar uang kuliah satu kali setiap semester dengan jumlah yang sama atau flat di setiap semesternya. Sedangkan sistem pembayaran biasa memungkinkan jumlah yang harus dibayarkan mahasiswa setiap semester berbeda-beda tergantung jumlah SKS yang diambil dan biaya lainnya.
</span>

<span style="font-weight: normal;" class = "trespon">
<b style="font-size: 30px">Bedanya UKT, BKT, dan SPI</b> <br>
Selain UKT, ada istilah yang masih berhubungan dengan biaya kuliah, yaitu BKT (Biaya Kuliah Tunggal) dan SPI (Sumbangan Pengembangan Institusi), apa perbedaan diantara ketiganya?<br><br>
1. BKT
Biaya Kuliah Tunggal (BKT) adalah keseluruhan biaya operasional per tahun yang terkait langsung dengan proses pembelajaran mahasiswa pada sebuah program studi di PTN. Besaran BKT dihitung berdasarkan program studi tanpa dikurangi subsidi pemerintah.
Sebenarnya, UKT adalah sebagian BKT yang ditanggungkan kepada mahasiswa sesuai kondisi ekonominya dan sebagian biaya lainnya ditanggung pemerintah.<br><br>
2. SPI
Sumbangan Pengembangan Institusi (SPI) adalah uang pangkal yang dibayarkan mahasiswa jalur mandiri pada awal perkuliahan. Besaran SPI ditentukan dari kemampuan ekonomi mahasiswa. Di beberapa kampus, SPI dikenal sebagai IPI (Iuran Pengembangan Institusi).
</span>


<span style="font-weight: normal; " class="trespon"> 
<b style="font-size: 30px">Bagaimana Cara Penentuan UKT?</b><br>
Program UKT hanya berlaku bagi mahasiswa PTN yang lolos melalui jalur SNBP/SNMPTN dan UTBK/SNBT. Bagi mahasiswa yang masuk melalui jalur mandiri akan mendapat besaran biaya kuliah yang sudah ditetapkan oleh perguruan tinggi masing-masing. Tapi jangan khawatir, di beberapa perguruan tinggi juga ada yang memberlakukan UKT untuk semua jalur masuk, misalnya Universitas Gadjah Mada (UGM).<br><br>

Terus, gimana sih cara penentuan kelompok UKT? Mahasiswa diharuskan mengisi formulir yang diberikan pihak kampus. Biasanya berisi pertanyaan mengenai gaji, tunjangan, atau penghasilan orang tua. Bukan hanya gaji dan tunjangan saja, tapi mahasiswa juga wajib memberikan informasi mengenai luas tanah, banyaknya kendaraan (mobil & motor), jumlah rumah, dan pengeluaran keluarga. Formulir ini harus diisi sejujur-jujurnya. Nah, setelah itu mahasiswa akan mendapatkan besaran UKT yang harus dibayarkan tiap semesternya.
</span>
